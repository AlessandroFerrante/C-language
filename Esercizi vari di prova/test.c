#include<stdio.h>

int main() {
    printf("Hello World!\n");// nuova riga
    printf("Hello \tWorld!\n");// tabulazione, aggiunge un tab
    printf("Hello \\World!\n");//slash
    printf("Hello \"World!\n");//virgolletta
    printf("Hello \a World!\n");//produce un suono o un alert visibile

}

/*
    Hello World!
    Hello   World!
    Hello \World!
    Hello "World!
    Hello World!
*/